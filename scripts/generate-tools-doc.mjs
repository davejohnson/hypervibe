#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const repoRoot = process.cwd();
const toolsDir = path.join(repoRoot, 'src', 'tools');
const outputPath = path.join(repoRoot, 'docs', 'TOOLS.md');

const files = fs
  .readdirSync(toolsDir)
  .filter((name) => name.endsWith('.tools.ts'))
  .sort();

const entries = [];
const toolRegex = /server\.tool\(\s*\n\s*'([^']+)'\s*,\s*\n\s*'([^']+)'/g;

for (const file of files) {
  const full = path.join(toolsDir, file);
  const source = fs.readFileSync(full, 'utf8');
  let match;
  while ((match = toolRegex.exec(source)) !== null) {
    entries.push({
      name: match[1],
      description: match[2],
      file,
    });
  }
}

entries.sort((a, b) => a.name.localeCompare(b.name));

const grouped = new Map();
for (const entry of entries) {
  if (!grouped.has(entry.file)) grouped.set(entry.file, []);
  grouped.get(entry.file).push(entry);
}

const generatedAt = new Date().toISOString();
let md = '';
md += '# Tool Catalog\n\n';
md += `Generated from source at \`${generatedAt}\`.\n\n`;
md += `Total tools: **${entries.length}**\n\n`;
md += '_Do not edit this file manually. Re-generate with `npm run docs:tools`._\n\n';

for (const [file, fileEntries] of grouped) {
  md += `## ${file}\n\n`;
  for (const tool of fileEntries) {
    md += `- \`${tool.name}\` - ${tool.description}\n`;
  }
  md += '\n';
}

fs.writeFileSync(outputPath, md, 'utf8');
console.log(`Wrote ${outputPath} (${entries.length} tools)`);
