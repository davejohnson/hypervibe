#!/usr/bin/env node
// Generates docs/TOOLS.md by introspecting the actual MCP server, so the
// catalog always matches what clients see. Requires a build first (npm run build).
import fs from 'node:fs';
import path from 'node:path';
import { mkdtempSync } from 'node:fs';
import os from 'node:os';

const repoRoot = process.cwd();
const outputPath = path.join(repoRoot, 'docs', 'TOOLS.md');
const serverModule = path.join(repoRoot, 'dist', 'server.js');

if (!fs.existsSync(serverModule)) {
  console.error('dist/server.js not found — run `npm run build` first.');
  process.exit(1);
}

// Point the sqlite store at a throwaway directory so doc generation never
// touches real state.
process.env.HYPERVIBE_DATA_DIR = mkdtempSync(path.join(os.tmpdir(), 'hypervibe-docs-'));

const { createServer } = await import(serverModule);
const { Client } = await import('@modelcontextprotocol/sdk/client/index.js');
const { InMemoryTransport } = await import('@modelcontextprotocol/sdk/inMemory.js');

const server = createServer();
const client = new Client({ name: 'docs-generator', version: '1.0.0' });
const [clientTransport, serverTransport] = InMemoryTransport.createLinkedPair();
await Promise.all([server.connect(serverTransport), client.connect(clientTransport)]);

const { tools } = await client.listTools();
tools.sort((a, b) => a.name.localeCompare(b.name));

const groups = new Map([
  ['Core (spec → plan → apply)', /^hv_(spec_|plan$|apply$|status$|import$|destroy$)/],
  ['Connections', /^hv_connect/],
  ['Deploy & observability', /^hv_(deploy|rollback|logs|errors|health)$/],
  ['Database', /^hv_db_/],
  ['Secrets', /^hv_secrets_/],
  ['Domains & email', /^hv_(domain_|dns_|email_)/],
  ['Payments', /^hv_(payments_|stripe_)/],
  ['CI', /^hv_ci_/],
  ['App Store & iOS', /^hv_(appstore_|testflight_|appid_|xcode_)/],
  ['Developer experience', /^hv_(upgrade|tunnel|local_bootstrap|visualize|runs)$/],
]);

const generatedAt = new Date().toISOString();
let md = '';
md += '# Tool Catalog\n\n';
md += `Generated from the live server at \`${generatedAt}\`.\n\n`;
md += `Total tools: **${tools.length}**\n\n`;
md += '_Do not edit this file manually. Re-generate with `npm run build && npm run docs:tools`._\n\n';

const seen = new Set();
for (const [heading, pattern] of groups) {
  const matched = tools.filter((t) => pattern.test(t.name) && !seen.has(t.name));
  if (matched.length === 0) continue;
  md += `## ${heading}\n\n`;
  for (const tool of matched) {
    seen.add(tool.name);
    const description = (tool.description ?? '').split('\n')[0];
    md += `- \`${tool.name}\` - ${description}\n`;
  }
  md += '\n';
}

const ungrouped = tools.filter((t) => !seen.has(t.name));
if (ungrouped.length > 0) {
  md += '## Other\n\n';
  for (const tool of ungrouped) {
    md += `- \`${tool.name}\` - ${(tool.description ?? '').split('\n')[0]}\n`;
  }
  md += '\n';
}

fs.writeFileSync(outputPath, md, 'utf8');
console.log(`Wrote ${outputPath} (${tools.length} tools)`);

await client.close();
await server.close();
process.exit(0);
